package com.jbb.mgt.core.domain.jsonformat.shebao;

public class Endowment_overview {
    private String begin_date;
    private String end_date;
    private String total_income_month;
    private String arrear_month;
    private String base_number;
    private String total_personal_income;
    private String total_company_income;
    private String balance;

    public String getBegin_date() {
        return begin_date;
    }

    public void setBegin_date(String begin_date) {
        this.begin_date = begin_date;
    }

    public String getEnd_date() {
        return end_date;
    }

    public void setEnd_date(String end_date) {
        this.end_date = end_date;
    }

    public String getTotal_income_month() {
        return total_income_month;
    }

    public void setTotal_income_month(String total_income_month) {
        this.total_income_month = total_income_month;
    }

    public String getArrear_month() {
        return arrear_month;
    }

    public void setArrear_month(String arrear_month) {
        this.arrear_month = arrear_month;
    }

    public String getBase_number() {
        return base_number;
    }

    public void setBase_number(String base_number) {
        this.base_number = base_number;
    }

    public String getTotal_personal_income() {
        return total_personal_income;
    }

    public void setTotal_personal_income(String total_personal_income) {
        this.total_personal_income = total_personal_income;
    }

    public String getTotal_company_income() {
        return total_company_income;
    }

    public void setTotal_company_income(String total_company_income) {
        this.total_company_income = total_company_income;
    }

    public String getBalance() {
        return balance;
    }

    public void setBalance(String balance) {
        this.balance = balance;
    }
}
